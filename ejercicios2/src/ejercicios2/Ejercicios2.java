package ejercicios2;
//Ejercicio variables primitivas 29 de Julio de 2020
public class Ejercicios2 {
    public static void main(String[] args) {
        //Bytes
        byte entero;
        byte entero2 = 5;
        entero = 12;
        
        System.out.println("Numero byte: "+entero);
        System.out.println("Numero byte 2: "+entero2);
        
        //Short
        short entero3 = 12456;
        System.out.println("Numero short: "+entero3);
        
        //Enteros
        int entero4 = 123456;
        System.out.println("Numero int: "+entero4);
        
        //Long
        long entero5 = 1234566;
        System.out.println("Numero long: "+entero5);
        
        //Float - se pone una f al final
        float decimal = 2.55f;
        System.out.println("Numero float: "+decimal);
        
        //Double
        double decimal2 = 9.85;
        System.out.println("Numero double: "+decimal2);
        
        //Char - caracter
        char a = 'a';
        System.out.println("Tipo caracter: "+a);
        
        //Booleano - true y false
        boolean desicion = true;
        System.out.println("La desici√≥n es: "+desicion);
    }
    
}
